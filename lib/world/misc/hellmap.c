int
command_pray ()
{
   int *xy = "/world/worldmap"->get_near_location(2640,5528,3);
   message (CMD_MSG, 0,0,0, "You pray and disappear.", PREV(1));
   PREV(1)->move("/world/worldmap",xy[0],xy[1]);
   PREV(1)->revive();
   PREV(1)->heal_hitlocs();
   PREV(1)->set_position(POS_STANDING);
   return 1;
}
